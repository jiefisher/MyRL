from dataclasses import dataclass, field
from typing import List, Optional

import torch

from .tool_parser import ParsedToolCall
from .tool_registry import ToolResult


@dataclass
class TurnRecord:
    turn_idx: int
    generated_token_ids: List[int]
    generated_text: str
    generation_logprobs: List[float]
    tool_call: Optional[ParsedToolCall] = None
    tool_result: Optional[ToolResult] = None
    tool_result_text: str = ""  # formatted tool result text
    tool_result_token_ids: List[int] = field(default_factory=list)
    is_final: bool = False


@dataclass
class Trajectory:
    original_prompt_tokens: List[int]
    ground_truth: str
    turns: List[TurnRecord] = field(default_factory=list)

    @property
    def num_turns(self) -> int:
        return len(self.turns)

    @property
    def is_complete(self) -> bool:
        return len(self.turns) > 0 and self.turns[-1].is_final

    @property
    def last_generated_text(self) -> str:
        if not self.turns:
            return ""
        return self.turns[-1].generated_text

    def total_generated_tokens(self) -> int:
        return sum(len(t.generated_token_ids) for t in self.turns)

    def total_tokens(self) -> int:
        """Total tokens including prompt, generations, and tool results."""
        total = len(self.original_prompt_tokens)
        for turn in self.turns:
            total += len(turn.generated_token_ids)
            total += len(turn.tool_result_token_ids)
        return total

    def to_training_tensors(self, max_seq_length: int, pad_token_id: int):
        """Flatten multi-turn trajectory into training tensors.

        Returns dict with:
            response_tokens: [max_seq_length] - full sequence (prompt + gen + tool_result + gen + ...)
            agent_loss_mask: [max_seq_length] - 1 for model-generated tokens, 0 for prompt/tool_result
            process_rewards: [max_seq_length] - per-token process rewards
            prompt_length: int
            response_length: int
            generation_logprobs: [max_seq_length] - logprobs for generated tokens, 0 elsewhere
        """
        # Build the full token sequence
        all_tokens = list(self.original_prompt_tokens)
        prompt_length = len(all_tokens)

        # Track which tokens are model-generated vs tool results
        loss_mask = [0.0] * prompt_length
        logprobs_flat = [0.0] * prompt_length
        process_rewards_flat = [0.0] * prompt_length

        for turn in self.turns:
            gen_len = len(turn.generated_token_ids)
            all_tokens.extend(turn.generated_token_ids)
            loss_mask.extend([1.0] * gen_len)
            # Pad logprobs if shorter than gen tokens (shouldn't happen, but be safe)
            turn_logprobs = turn.generation_logprobs
            if len(turn_logprobs) < gen_len:
                turn_logprobs = turn_logprobs + [0.0] * (gen_len - len(turn_logprobs))
            logprobs_flat.extend(turn_logprobs[:gen_len])
            # Process rewards are distributed later by AgentRewardComputer
            process_rewards_flat.extend([0.0] * gen_len)

            # Tool result tokens (not generated by model)
            if turn.tool_result_token_ids:
                tr_len = len(turn.tool_result_token_ids)
                all_tokens.extend(turn.tool_result_token_ids)
                loss_mask.extend([0.0] * tr_len)
                logprobs_flat.extend([0.0] * tr_len)
                process_rewards_flat.extend([0.0] * tr_len)

        response_length = len(all_tokens)

        # Pad or truncate to max_seq_length
        if len(all_tokens) > max_seq_length:
            all_tokens = all_tokens[:max_seq_length]
            loss_mask = loss_mask[:max_seq_length]
            logprobs_flat = logprobs_flat[:max_seq_length]
            process_rewards_flat = process_rewards_flat[:max_seq_length]
            response_length = max_seq_length
        else:
            pad_len = max_seq_length - len(all_tokens)
            all_tokens.extend([pad_token_id] * pad_len)
            loss_mask.extend([0.0] * pad_len)
            logprobs_flat.extend([0.0] * pad_len)
            process_rewards_flat.extend([0.0] * pad_len)

        return {
            "response_tokens": torch.LongTensor(all_tokens),
            "agent_loss_mask": torch.FloatTensor(loss_mask),
            "process_rewards": torch.FloatTensor(process_rewards_flat),
            "generation_logprobs": torch.FloatTensor(logprobs_flat),
            "prompt_length": prompt_length,
            "response_length": response_length,
        }
